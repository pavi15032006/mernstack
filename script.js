// var example (can redeclare and reassign)
var a = 5;
var a = 10;
console.log(a);   // Output: 10

// let example (cannot redeclare, but can reassign)
let b = 20;
b = 30;
console.log(b);   // Output: 30

// const example (cannot redeclare and cannot reassign)
const c = 40;
console.log(c);   